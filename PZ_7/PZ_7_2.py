#Дана строка-предложение. Зашифровать ее, поместив вначале все символы,
#расположенные на четных позициях строки, а затем, в обратном порядке, все
#символы, расположенные на нечетных позициях (например, строка «Программа»
#превратится в «ргамамроП»)
s = input("Введите строку: ")

even_list = []  #символы с чётных позиций
odd_list = []   #символы с нечётных позиций

for i in range(len(s)):
    if (i + 1) % 2 == 0:      #позиция чётная (считаем с 1)
        even_list.append(s[i])
    else:                      #позиция нечётная
        odd_list.append(s[i])

odd_list.reverse()             #разворачиваем список нечётных символов

result_list = even_list + odd_list   #склеиваем два списка
print("".join(result_list))