#Если в матрице имеются положительные элементы, то вывести TRUE, иначе FALSE.
import random
# Генерируем матрицу 3x4 со случайными числами от -10 до 10
matrix = [[random.randint(-10, 2) for _ in range(4)] for _ in range(3)]

# Выводим исходную матрицу, каждая строка с новой строки
print("Исходная матрица:")
for row in matrix:
    print(row)

# Проверяем наличие положительных элементов через генераторное выражение:
# генератор перебирает все элементы матрицы (строка за строкой),
# any() возвращает True, как только найдёт первый элемент > 0

result = any(elem > 0 for row in matrix for elem in row)

print("Результат:", result)
